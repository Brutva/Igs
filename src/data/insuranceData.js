export function getAnswerLabel(scenarioId, stepId, optionId) {
  const scenario = scenarios.find((item) => item.id === scenarioId);
  const step = scenario?.steps.find((item) => item.id === stepId);
  return step?.options.find((item) => item.id === optionId)?.label || '—';
}

function clamp(value, min, max) {
  return Math.min(max, Math.max(min, value));
}

function scoreToRisk(score) {
  if (score >= 70) {
    return { riskEmoji: '🔴', riskLabel: 'Высокий', recommendationLevel: 'Стоит рассмотреть всерьёз' };
  }
  if (score >= 45) {
    return { riskEmoji: '🟡', riskLabel: 'Средний', recommendationLevel: 'Есть смысл сравнить варианты' };
  }
  return { riskEmoji: '🟢', riskLabel: 'Низкий', recommendationLevel: 'Скорее дополнительная защита' };
}

function buildAnswerSummary(scenarioId, answers) {
  const scenario = scenarios.find((item) => item.id === scenarioId);
  return (scenario?.steps || []).map((step) => ({
    label: step.question,
    value: getAnswerLabel(scenarioId, step.id, answers?.[step.id]),
  }));
}

